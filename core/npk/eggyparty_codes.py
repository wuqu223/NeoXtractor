def decrypt_mode3_block(b, k):
    T, S, M = [AES_DEC_T0, AES_DEC_T1, AES_DEC_T2, AES_DEC_T3], AES_INV_SBOX, 0xFFFFFFFF
    # N (NeoX Transform): Handles the custom byte-swap mask (rot8 ^ mask)
    N = lambda v, r=0: (
        (rk := (v >> 8 | v << 24) & M) ^ r ^ ((rk ^ (v << 8 | v >> 24) & M) & 0xFF00FF)
    )

    x = struct.unpack("<4I", b)
    s = [N(x[i], k[i]) for i in range(4)]
    # Initial state mapping: a=v17, b=v18, c=v19, d=v21
    a = (
        k[4]
        ^ T[0][s[1] & 255]
        ^ T[1][s[0] >> 24]
        ^ T[2][s[3] >> 16 & 255]
        ^ T[3][s[2] >> 8 & 255]
    ) & M
    b = (
        k[5]
        ^ T[0][s[2] & 255]
        ^ T[1][s[1] >> 24]
        ^ T[3][s[3] >> 8 & 255]
        ^ T[2][s[0] >> 16 & 255]
    ) & M
    c = (
        k[6]
        ^ T[0][s[3] & 255]
        ^ T[1][s[2] >> 24]
        ^ T[3][s[0] >> 8 & 255]
        ^ T[2][s[1] >> 16 & 255]
    ) & M
    d = (
        k[7]
        ^ T[0][s[0] & 255]
        ^ T[1][s[3] >> 24]
        ^ T[2][s[2] >> 16 & 255]
        ^ T[3][s[1] >> 8 & 255]
    ) & M

    j = 8
    for _ in range((k[60] >> 1) - 1):
        w, x, y, z = (
            (
                k[j + 3]
                ^ T[0][a & 255]
                ^ T[1][d >> 24]
                ^ T[2][c >> 16 & 255]
                ^ T[3][b >> 8 & 255]
            )
            & M,
            (
                k[j + 2]
                ^ T[0][d & 255]
                ^ T[1][c >> 24]
                ^ T[2][b >> 16 & 255]
                ^ T[3][a >> 8 & 255]
            )
            & M,
            (
                k[j + 1]
                ^ T[0][c & 255]
                ^ T[1][b >> 24]
                ^ T[3][d >> 8 & 255]
                ^ T[2][a >> 16 & 255]
            )
            & M,
            (
                k[j + 0]
                ^ T[0][b & 255]
                ^ T[1][a >> 24]
                ^ T[2][d >> 16 & 255]
                ^ T[3][c >> 8 & 255]
            )
            & M,
        )
        a, b, c, d = (
            (
                k[j + 4]
                ^ T[0][y & 255]
                ^ T[1][z >> 24]
                ^ T[2][w >> 16 & 255]
                ^ T[3][x >> 8 & 255]
            )
            & M,
            (
                k[j + 5]
                ^ T[0][x & 255]
                ^ T[1][y >> 24]
                ^ T[3][w >> 8 & 255]
                ^ T[2][z >> 16 & 255]
            )
            & M,
            (
                k[j + 6]
                ^ T[0][w & 255]
                ^ T[1][x >> 24]
                ^ T[2][y >> 16 & 255]
                ^ T[3][z >> 8 & 255]
            )
            & M,
            (
                k[j + 7]
                ^ T[0][z & 255]
                ^ T[1][w >> 24]
                ^ T[2][x >> 16 & 255]
                ^ T[3][y >> 8 & 255]
            )
            & M,
        )
        j += 8
    r = [
        (
            k[j]
            ^ S[b & 255]
            ^ S[c >> 8 & 255] << 8
            ^ S[d >> 16 & 255] << 16
            ^ S[a >> 24 & 255] << 24
        )
        & M,
        (
            k[j + 1]
            ^ S[c & 255]
            ^ S[d >> 8 & 255] << 8
            ^ S[a >> 16 & 255] << 16
            ^ S[b >> 24 & 255] << 24
        )
        & M,
        (
            k[j + 2]
            ^ S[d & 255]
            ^ S[a >> 8 & 255] << 8
            ^ S[b >> 16 & 255] << 16
            ^ S[c >> 24 & 255] << 24
        )
        & M,
        (
            k[j + 3]
            ^ S[a & 255]
            ^ S[b >> 8 & 255] << 8
            ^ S[c >> 16 & 255] << 16
            ^ S[d >> 24 & 255] << 24
        )
        & M,
    ]

    return struct.pack("<4I", *[N(i) for i in r])
